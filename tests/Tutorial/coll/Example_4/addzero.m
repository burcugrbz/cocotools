function [data y] = addzero(opts, data, xp)

x10 = xp(1:3,:);
x20 = xp(4:6,:);
p10 = xp(7:9,:);
e10 = xp(10:12,:);
p20 = xp(13:15,:);
e20 = xp(16:18,:);

vu = -[1-p10(1,:)+sqrt((1-p10(1,:)).^2+4*p10(1,:).*p10(2,:)); 2*p10(2,:); 0];
vs1 = [1-p10(1,:)-sqrt((1-p10(1,:)).^2+4*p10(1,:).*p10(2,:)); 2*p10(2,:); 0];
vs1 = vs1/(vs1'*vs1);
vs2 = [0; 0; 1];

y = [p10 - p20 ; e10 - e20 ; x10 - e10(1,:).*vu ; x20 - e10(2,:).*(cos(e10(3,:)).*vs1+sin(e10(3,:)).*vs2)];

end